// Declararea unei variabile folosind var

var students = 14;
var session = 'JS Introduction';

// Logging in browser al valorilor

console.log(students);
console.log(session);

// Schimbarea valorii stocate in variabila

var students = 150;
console.log(students);

// Declararea unei variabile folosind let

let studentsLet = 15;
console.log(studentsLet);

studentsLet = 150;
console.log(studentsLet);

// Declararea unei variabile folosind const

const studentsConst = 100;
console.log(studentsConst);
